function previous( fHiLight ) /* function for showing the prev month */
{
	if (c_year >= 2007) /* previous button will not work if dec 2006 is reached */
	{
		if (c_month == 0) 
			c_month = 11;
		else 
			c_month--;
		if (c_month == 11) 
			c_year--;
		ptimetable("pt", c_month, fHiLight);
	}
}

